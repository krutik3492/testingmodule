
public class natrualnumber {

	public static void main(String[] args) {
        // Using a for loop to print the first 10 natural numbers
        for (int i = 1; i <= 10; i++) {
            System.out.println(i + " ");
        }
        
        int j = 1;
        
        // Using a while loop to print the first 10 natural numbers
        while (j <= 10) {
            System.out.print(j + " ");
            j++;
        }
        System.out.println();
        
int k = 1;
        
        // Using a do-while loop to print the first 10 natural numbers
        do {
            System.out.print(k + " ");
            k++;
        } while (k <= 10);
}
}
